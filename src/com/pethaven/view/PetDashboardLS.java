    private void btnFindPetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindPetActionPerformed
try {
        // 1. Get the ID from the search box
        int targetId = Integer.parseInt(txtSearch.getText()); 

        // 2. Binary Search requires a sorted list by ID 
        pc.sortByID(petList); 

        // 3. Run the search
        int resultIndex = pc.binarySearchByID(petList, targetId);

        if (resultIndex != -1) {
            // Success! Store the index so we can "Update" it later
            currentFoundIndex = resultIndex; 
            
            com.pethaven.model.PetModel found = petList.get(resultIndex);
            
            // 4. Set the text in your display fields
            txtIDDisplay.setText(String.valueOf(found.getPetId()));
            txtNameDisplay.setText(found.getName());
            txtAgeDisplay.setText(String.valueOf(found.getAge()));
            txtBreedDisplay.setText(found.getBreed());
            
            JOptionPane.showMessageDialog(this, "Pet Found: " + found.getName());
        } else {
            currentFoundIndex = -1;
            JOptionPane.showMessageDialog(this, "Pet ID not found in the system.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid numeric ID to search.");}    }//GEN-LAST:event_btnFindPetActionPerformed

    private void btnUpdatePetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePetActionPerformed
if (currentFoundIndex != -1) {
        try {
            // 1. Get the new info from your editable fields
            String newName = txtNameDisplay.getText();
            int newAge = Integer.parseInt(txtAgeDisplay.getText());

            // 2. Update the list at the index we found earlier
            petList.get(currentFoundIndex).setName(newName);
            petList.get(currentFoundIndex).setAge(newAge);

            // 3. Save to file and refresh the UI
            pc.savePetsToFile(petList);
            refreshTable();

            JOptionPane.showMessageDialog(this, "Pet Updated Successfully!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for Age.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please search for a pet first!");
    }
    }//GEN-LAST:event_btnUpdatePetActionPerformed

