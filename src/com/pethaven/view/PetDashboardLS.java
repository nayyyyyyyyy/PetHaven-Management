    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
if (petList.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Add some pets first!");
        return;
    }

    String selected = cmbSortChoice.getSelectedItem().toString();
    txtLogArea.append("\n--- Running: " + selected + " ---");
    
    long startTime = System.nanoTime();

    if (selected.contains("ID")) {
        pc.sortByID(petList); // Selection Sort [cite: 2026-01-15]
        txtLogArea.append("\n Sorted by ID using Selection Sort.");
    } else if (selected.contains("Name")) {
        pc.sortByName(petList); // Insertion Sort [cite: 2026-01-15]
        txtLogArea.append("\n Sorted by Name using Insertion Sort.");
    } else if (selected.contains("Age")) {
        pc.sortByAge(petList); // Merge Sort [cite: 2026-01-15]
        txtLogArea.append("\n Sorted by Age using Merge Sort.");
    }

    long endTime = System.nanoTime();
    double duration = (endTime - startTime) / 1000000.0;
    txtLogArea.append("\n Time: " + duration + " ms\n");

    refreshTable(); // Updates your main table!    }//GEN-LAST:event_jButton1ActionPerformed
    }
